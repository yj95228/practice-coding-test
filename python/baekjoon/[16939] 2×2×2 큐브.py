import sys
input = sys.stdin.readline

def solve():
    for original, change in rotate:
        ncube = cube[:]
        for i in range(8):
            ncube[original[i]] = cube[change[i]]
        for i in range(0, 24, 4):
            if ncube[i] == ncube[i + 1] == ncube[i + 2] == ncube[i + 3]: continue
            break
        else: return 1
    return 0

